# Conversión de cadena a entero
cadena_a_entero = "42"
entero = int(cadena_a_entero)
print(f"Cadena a entero: {entero} (Tipo: {type(entero)})")

# Conversión de cadena a decimal
cadena_a_decimal = "3.14"
decimal = float(cadena_a_decimal)
print(f"Cadena a decimal: {decimal} (Tipo: {type(decimal)})")

# Conversión de entero a cadena
entero_a_cadena = str(entero)
print(f"Entero a cadena: {entero_a_cadena} (Tipo: {type(entero_a_cadena)})")
